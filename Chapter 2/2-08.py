# How about some whitespace addition to strings?

# Let's just see Python.
print("Python")

# Now let's tab that over using \t
print("\tPython")

# How about inserting some newlines using \n
print("Languages:\nPython\nC\nJavascript")

# Put them both together and what do you get?
print("Languages:\n\tPython\n\tC\n\tJavascript")